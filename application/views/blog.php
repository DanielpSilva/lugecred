            <h2 class="entry-title" style="color: #140152">
              Empréstimo pessoal ou consignado?
            </h2>

            <div class="entry-meta">
              <ul>
                <li class="d-flex align-items-center"><i class="icofont-wall-clock"></i>Jan 1, 2020</li>
                <li class="d-flex align-items-center"><i class="icofont-comment"></i> <a href=""></a></li>
              </ul>
            </div>

            <div class="entry-content text-justify">
              <p>
                O empréstimo consignado é direcionado para aposentados e pensionistas do INSS, inclui também servidores públicos, caracterizado por cobrar taxas de juros inferiores a 1,80%, além de ser descontado direto da folha de pagamento, o empréstimo é realizado através de confirmações bancárias com base no valor disponível na margem consignável e conforme a necessidade.
                <br> Entretanto, o refinanciamento consiste em uma dívida já existente que possibilita resgatar uma restituição monetária, sem alterar o valor das parcelas, ou seja, o valor resgatado não acarretará nenhum acréscimo no valor descontado na folha de pagamento, pois é feita a quitação do saldo devedor do antigo contrato e a diferença é devolvida ao titular da dívida.
                <br> O empréstimo pessoal está disponível para qualquer pessoa sob análise de documentação para definir os valores liberados, após as liberações o dinheiro cai diretamente na sua conta, as taxas de juros podem variar de uma instituição financeira para outra assim como o valor da parcela e a forma de pagamento.
                <br> São várias opções de créditos para se adequar conforme a sua necessidade.

              </p>
